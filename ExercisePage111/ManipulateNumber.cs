using System;
namespace ExercisePage111
{
    public static class ManipulateNumber
    {
        // method 1: add 10 to InputNumber
        public static int Add10(int inputNumber)
        {
            return inputNumber + 10;
        }

        // method 2: subtract 2 from InputNumber
        public static int Subtract2(int inputNumber)
        {
            return inputNumber - 2;
        }

        // method 3: multiply InputNumber by 5
        public static int Multiply5(int inputNumber)
        {
            return inputNumber * 5;
        }

    }
}
